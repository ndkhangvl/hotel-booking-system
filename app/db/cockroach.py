def create_all_tables():
    tables = [
        ("users", """
        CREATE TABLE IF NOT EXISTS users (
            user_id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
            name VARCHAR(100) NOT NULL,
            email VARCHAR(150) UNIQUE NOT NULL,
            phone VARCHAR(15),
            password VARCHAR(255) NOT NULL,
            role VARCHAR(20) NOT NULL CHECK (role IN ('Guest', 'Customer', 'Receptionist', 'Admin')),
            created_date DATE DEFAULT CURRENT_DATE,
            created_time TIME DEFAULT CURRENT_TIME,
            created_user UUID,
            updated_date DATE,
            updated_time TIME,
            updated_user UUID,
            del_flg SMALLINT DEFAULT 0
        );
        """),

        ("branches", """
        CREATE TABLE IF NOT EXISTS branches (
            branch_id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
            name VARCHAR(100) NOT NULL,
            address VARCHAR(255) NOT NULL,
            phone VARCHAR(15),
            created_date DATE DEFAULT CURRENT_DATE,
            created_time TIME DEFAULT CURRENT_TIME,
            created_user UUID,
            updated_date DATE,
            updated_time TIME,
            updated_user UUID,
            del_flg SMALLINT DEFAULT 0
        );
        """),

        ("room_types", """
        CREATE TABLE IF NOT EXISTS room_types (
            room_type_id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
            name VARCHAR(50) NOT NULL,
            description TEXT,
            created_date DATE DEFAULT CURRENT_DATE,
            created_time TIME DEFAULT CURRENT_TIME,
            created_user UUID,
            updated_date DATE,
            updated_time TIME,
            updated_user UUID,
            del_flg SMALLINT DEFAULT 0
        );
        """),

        ("amenities", """
        CREATE TABLE IF NOT EXISTS amenities (
            amenity_id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
            name VARCHAR(100) NOT NULL,
            icon_url VARCHAR(255),
            created_date DATE DEFAULT CURRENT_DATE,
            created_time TIME DEFAULT CURRENT_TIME,
            created_user UUID,
            updated_date DATE,
            updated_time TIME,
            updated_user UUID,
            del_flg SMALLINT DEFAULT 0
        );
        """),

        ("rooms", """
        CREATE TABLE IF NOT EXISTS rooms (
            room_id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
            branch_id UUID NOT NULL,
            room_type_id UUID NOT NULL,
            room_number VARCHAR(20) NOT NULL,
            price DECIMAL(10, 2) NOT NULL,
            people_number INT NOT NULL,
            created_date DATE DEFAULT CURRENT_DATE,
            created_time TIME DEFAULT CURRENT_TIME,
            created_user UUID,
            updated_date DATE,
            updated_time TIME,
            updated_user UUID,
            del_flg SMALLINT DEFAULT 0,

            CONSTRAINT fk_rooms_branch
                FOREIGN KEY (branch_id) REFERENCES branches(branch_id),
            CONSTRAINT fk_rooms_room_type
                FOREIGN KEY (room_type_id) REFERENCES room_types(room_type_id)
        );
        """),

        ("vouchers", """
        CREATE TABLE IF NOT EXISTS vouchers (
            voucher_code VARCHAR(20) PRIMARY KEY,
            discount_value DECIMAL(10, 2) NOT NULL,
            valid_from TIMESTAMP NOT NULL,
            valid_to TIMESTAMP NOT NULL,
            created_date DATE DEFAULT CURRENT_DATE,
            created_time TIME DEFAULT CURRENT_TIME,
            created_user UUID,
            updated_date DATE,
            updated_time TIME,
            updated_user UUID,
            del_flg SMALLINT DEFAULT 0
        );
        """),

        ("bookings", """
        CREATE TABLE IF NOT EXISTS bookings (
            booking_id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
            user_id UUID NOT NULL,
            room_id UUID NOT NULL,
            voucher_code VARCHAR(20),
            from_date DATE NOT NULL,
            to_date DATE NOT NULL,
            total_price DECIMAL(10, 2) NOT NULL,
            status VARCHAR(20) NOT NULL DEFAULT 'Pending',
            created_date DATE DEFAULT CURRENT_DATE,
            created_time TIME DEFAULT CURRENT_TIME,
            created_user UUID,
            updated_date DATE,
            updated_time TIME,
            updated_user UUID,
            del_flg SMALLINT DEFAULT 0,

            CONSTRAINT fk_bookings_user
                FOREIGN KEY (user_id) REFERENCES users(user_id),
            CONSTRAINT fk_bookings_room
                FOREIGN KEY (room_id) REFERENCES rooms(room_id),
            CONSTRAINT fk_bookings_voucher
                FOREIGN KEY (voucher_code) REFERENCES vouchers(voucher_code),
            CONSTRAINT chk_booking_dates
                CHECK (to_date > from_date)
        );
        """),

        ("room_amenities", """
        CREATE TABLE IF NOT EXISTS room_amenities (
            room_id UUID NOT NULL,
            amenity_id UUID NOT NULL,
            created_date DATE DEFAULT CURRENT_DATE,
            created_time TIME DEFAULT CURRENT_TIME,
            created_user UUID,
            updated_date DATE,
            updated_time TIME,
            updated_user UUID,
            del_flg SMALLINT DEFAULT 0,

            PRIMARY KEY (room_id, amenity_id),
            CONSTRAINT fk_room_amenities_room
                FOREIGN KEY (room_id) REFERENCES rooms(room_id),
            CONSTRAINT fk_room_amenities_amenity
                FOREIGN KEY (amenity_id) REFERENCES amenities(amenity_id)
        );
        """),

        ("reviews", """
        CREATE TABLE IF NOT EXISTS reviews (
            review_id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
            booking_id UUID NOT NULL,
            rating INT NOT NULL CHECK (rating >= 1 AND rating <= 5),
            comment TEXT,
            created_date DATE DEFAULT CURRENT_DATE,
            created_time TIME DEFAULT CURRENT_TIME,
            created_user UUID,
            updated_date DATE,
            updated_time TIME,
            updated_user UUID,
            del_flg SMALLINT DEFAULT 0,

            CONSTRAINT fk_reviews_booking
                FOREIGN KEY (booking_id) REFERENCES bookings(booking_id)
        );
        """),

        ("payments", """
        CREATE TABLE IF NOT EXISTS payments (
            payment_id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
            booking_id UUID NOT NULL,
            amount DECIMAL(10, 2) NOT NULL,
            status VARCHAR(20) NOT NULL DEFAULT 'Pending',
            created_date DATE DEFAULT CURRENT_DATE,
            created_time TIME DEFAULT CURRENT_TIME,
            created_user UUID,
            updated_date DATE,
            updated_time TIME,
            updated_user UUID,
            del_flg SMALLINT DEFAULT 0,

            CONSTRAINT fk_payments_booking
                FOREIGN KEY (booking_id) REFERENCES bookings(booking_id)
        );
        """)
    ]

    try:
        with get_connection() as conn:
            with conn.cursor() as cur:
                for table_name, ddl in tables:
                    cur.execute(ddl)
                    print(f"✅ Tạo bảng {table_name} thành công")
            conn.commit()
        print("🎉 Tạo toàn bộ bảng thành công")
    except Exception as e:
        print("❌ Lỗi tạo bảng:", e)

